export class Operaciones {

    sumar(a:number,b:number){
        return a+b;
    }
    
    
    restar(a:number,b:number){
        return a-b;
    }
    
    
    multiplicar(a:number,b:number){
        return a*b;
    }
    
    
    dividir(a:number,b:number){
        return a/b;
    }
    
    
    }